import mongoose from 'mongoose';

const AddOnSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
    min: 0,
  },
  image: {
    type: String,
  },
  isActive: {
    type: Boolean,
    default: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

if (mongoose.models.AddOn) {
    delete mongoose.models.AddOn;
}

export default mongoose.model('AddOn', AddOnSchema);
